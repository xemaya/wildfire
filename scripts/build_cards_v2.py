def main():
    data = json.load(open(SRC, encoding="utf-8"))
    out = {}
    rows = []
    missing = []
    for cat, cd in data.items():
        pre = PRE[cat]
        cards_out = []
        for idx, c in enumerate(cd["cards"], 1):
            cid = f"{pre}-{idx:02d}"
            r = R.get(cid)
            if not r:
                missing.append(cid); continue
            title = r.get("t", c["title"])
            card = {
                "id": cid,
                "title": title,
                "vol": c.get("vol", ""),
                "desc": r.get("desc", c.get("desc", "")),
                "action": r.get("act", c.get("action", "")),
                "src": r["src"],
                "intent": r["i"],
                "scene": r["s"],
            }
            if "old" in r: card["oldTitle"] = r["old"]
            if "n" in r:   card["note"] = r["n"]
            cards_out.append(card)
            rows.append((cat, cid, r.get("old"), title, r["src"], r["i"], r["s"], r.get("n","")))
        out[cat] = {"en": cd.get("en",""), "sym": cd.get("sym",""), "cards": cards_out}

    json.dump(out, open(f"{ROOT}/data/cards_v2.json","w",encoding="utf-8"),
              ensure_ascii=False, indent=2)

    # 改动对照表
    md = ["# Phase 2 · 卡牌重审改动对照表\n",
          "> 每张卡的 `出处` 对应 `references/核心思想.md` 中的条目。",
          "> 规则：困境/负面卡不出现战士正面或狼狈形象；名字须毛选正统；立意须有据。\n"]
    renamed = [r for r in rows if r[2]]
    md.append(f"## 改名 / 替换（{len(renamed)} 张）\n")
    md.append("| id | 原名 | 新名 | 理由 |\n|---|---|---|---|")
    for cat,cid,old,title,src,i,s,n in renamed:
        md.append(f"| {cid} | {old} | **{title}** | {n} |")
    md.append("")
    for cat in data.keys():
        md.append(f"\n## {cat}\n")
        md.append("| id | 卡名 | 出处 | 立意 | 画面 |\n|---|---|---|---|---|")
        for r in rows:
            if r[0]!=cat: continue
            cid,old,title,src,i,s = r[1],r[2],r[3],r[4],r[5],r[6]
            nm = f"**{title}**" + (f" ←{old}" if old else "")
            md.append(f"| {cid} | {nm} | {src} | {i} | {s} |")
    open(f"{ROOT}/改动对照表.md","w",encoding="utf-8").write("\n".join(md)+"\n")

    print(f"cards_v2.json: {sum(len(v['cards']) for v in out.values())} 张")
    print(f"改名/替换: {len(renamed)} 张 ->", ", ".join(f"{r[2]}→{r[3]}" for r in renamed))
    if missing: print("缺失:", missing)
    else: print("无缺失，全部 150 张已覆盖")

main()
