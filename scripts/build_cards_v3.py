changes=[]
v3={}
for cat,cd in v2.items():
    cards=[]
    for c in cd["cards"]:
        if c["id"] in DELETE:
            changes.append(("删除",c["id"],c["title"],""));continue
        c=dict(c)
        ov=OV.get(c["id"])
        if ov:
            before=c["title"]
            for k_src,k_dst in [("t","title"),("desc","desc"),("act","action"),
                                ("src","src"),("i","intent"),("s","scene")]:
                if k_src in ov: c[k_dst]=ov[k_src]
            if "n" in ov: c["note"]=ov["n"]
            if "t" in ov and ov["t"]!=before:
                c["oldTitle"]=before
                changes.append(("改名",c["id"],f"{before}→{ov['t']}",ov.get("n","")))
            else:
                changes.append(("改画面/出处",c["id"],c["title"],ov.get("n","")))
        cards.append(c)
    for nc in ADD.get(cat,[]):
        cards.append(nc); changes.append(("新增",nc["id"],nc["title"],nc.get("note","")))
    v3[cat]={"en":cd["en"],"sym":cd["sym"],"cards":cards}

